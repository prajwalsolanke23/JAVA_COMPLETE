public class TypeConversion {

    public static void main(String[] args) {

        float myFloat = 5;
        double myDouble = 5;

        System.out.println(myFloat); // 5.0
        System.out.println(myDouble); // 5.0

        // converting float to int

        int myInt = (int) 5.45f;
        System.out.println(myInt); // 5
    }
}
