public class Variables {

    public static void main(String[] args) {

        int myNumber = 865; // initialization
        int yourNumber; // decalaration

//        System.out.println(yourNumber); // error: u did not initialize the variable

        yourNumber = 865;

        System.out.println(yourNumber); // 865

        yourNumber = 45;

        System.out.println(yourNumber); // 45

        // float

        float myFloat = 5.0f; // error: it's an double

        System.out.println(myFloat);

        // double

        double myDouble = 6.84584594;

        System.out.println(myDouble);

        // String

        String wishes = "Good Morning";

        System.out.println(wishes);

        // char

        char myCharacter = 'K';

        System.out.println(myCharacter);


        // boolean

        boolean isVegetarian = false;

        System.out.println(isVegetarian); // false
    }
}
